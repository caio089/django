function getAllWords() {
  const seen = new Set();
  const all = [];
  ['gray', 'blue', 'yellow', 'green'].forEach((key) => {
    BELT_DATA[key].words.forEach((w) => {
      const id = `${w.japanese}-${w.meaning}`;
      if (!seen.has(id)) {
        seen.add(id);
        all.push(w);
      }
    });
  });
  return all;
}

// Preencher brown e purple
